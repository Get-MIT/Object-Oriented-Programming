
public class Student {
	
	private String name;
	private String id;
	
	// Constructor with 2 String parameters
	public Student(String aName, String anID) {
		name = aName;
		id = anID;
	}
	
	// Constructor with 1 String parameter
	public Student(String aName) {
		name = aName;
		id = "9999";
	}

}
